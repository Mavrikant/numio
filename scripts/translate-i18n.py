def apply_translations(text, lang_dict):
    """Replace known English words with their translations."""
    result = text
    for src, tgt in sorted(lang_dict.items(), key=lambda x: -len(x[0])):
        result = result.replace(src, tgt)
    return result


def find_en_block_boundaries(content):
    """Find where the en block starts and ends. Returns (start, end) or None."""
    idx = content.find('\n  en: {')
    if idx < 0:
        idx = content.find('  en: {')
    if idx < 0:
        return None
    
    brace_start = content.index('{', idx)
    depth = 0
    for i in range(brace_start, len(content)):
        if content[i] == '{': depth += 1
        elif content[i] == '}':
            depth -= 1
            if depth == 0:
                return (idx, i + 1)
    return None


def process_file(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Skip if already processed
    if '  nl: {' in content:
        return True
    
    bounds = find_en_block_boundaries(content)
    if bounds is None:
        return False
    start, end = bounds
    
    en_block = content[start:end]
    
    # Create nl block by replacing en: with nl: and translating strings
    nl_block = en_block.replace('  en: {', '  nl: {')
    # Only translate content INSIDE quotes (between " " or ' ')
    # Simple approach: split by " and translate every other segment
    parts = nl_block.split('"')
    for i in range(1, len(parts), 2):
        if i < len(parts):
            # This is a quoted string - translate it
            parts[i] = apply_translations(parts[i], EN_NL)
    nl_block = '"'.join(parts)
    nl_block = f'{nl_block},\n'
    
    # Same for pl
    pl_block = en_block.replace('  en: {', '  pl: {')
    parts = pl_block.split('"')
    for i in range(1, len(parts), 2):
        if i < len(parts):
            parts[i] = apply_translations(parts[i], EN_PL)
    pl_block = '"'.join(parts)
    pl_block = f'{pl_block},\n'
    
    new_content = content[:end] + nl_block + pl_block + content[end:]
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    return True


def main():
    base = sys.argv[1] if len(sys.argv) > 1 else '/Users/serdar/numio'
    
    all_files = []
    all_files.extend(glob.glob(os.path.join(base, 'src/tools/*/i18n.ts')))
    all_files.extend(glob.glob(os.path.join(base, 'src/calculators/**/i18n.ts'), recursive=True))
    
    print(f"Total: {len(all_files)} files")
    
    ok, skip, fail = 0, 0, 0
    for fp in all_files:
        try:
            if process_file(fp):
                ok += 1
            else:
                skip += 1
        except Exception as e:
            print(f"  FAIL: {os.path.basename(os.path.dirname(fp))}: {e}")
            fail += 1
            if fail > 5:
                print("Too many failures, aborting")
                break
        if ok % 100 == 0 and ok > 0:
            print(f"  Progress: {ok}/{len(all_files)}")
    
    print(f"\n✓ {ok} | ↷ {skip} skipped | ✗ {fail} failed")


if __name__ == '__main__':
    main()