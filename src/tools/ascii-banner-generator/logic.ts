/** Render a string as 5-row ASCII art using the bundled block font. */
export function asciiBanner(input: string, charset: "block" | "hash" | "dot" = "block"): string {
  const text = input.toUpperCase();
  const rows = ["", "", "", "", ""];
  for (const ch of text) {
    const glyph = FONT[ch] ?? FONT[" "]!;
    for (let r = 0; r < 5; r++) rows[r] += glyph[r];
  }
  if (charset === "hash") return rows.join("\n");
  if (charset === "dot") return rows.join("\n").replace(/#/g, "·");
  return rows.join("\n").replace(/#/g, "█");
}
